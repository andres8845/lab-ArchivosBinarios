    private void AgregarEmpledoBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarEmpledoBotonActionPerformed
        if(name.getText().isEmpty() || salary.getText().isEmpty()){
           JOptionPane.showMessageDialog(this,"Favor rellenar todas las casillas!");
        }else{
            String Name=name.getText();
            
            try{
                double Salary=Double.parseDouble(salary.getText()); 
                em.addEmployee(Name,Salary);
                JOptionPane.showMessageDialog(null, "Se ha agregado empleado correctamente!");
                name.setText("");
                salary.setText("");
                addEmployee.setVisible(false);
                menu.setVisible(true);
            }catch(IOException e){
                JOptionPane.showMessageDialog(null, "Algo salio mal #.#");
            }catch(InputMismatchException | NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Recuerde Ingresar un valor numerico Oo_oO");
            }
            
        }
    }//GEN-LAST:event_AgregarEmpledoBotonActionPerformed

