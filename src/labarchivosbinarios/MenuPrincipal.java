    private void AgregarVentaBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarVentaBotonActionPerformed
        if (Codigo.getText().isEmpty() || Venta.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Favor rellenar todas las casillas!");
        }else{
            try{
                int codigo=Integer.valueOf(Codigo.getText());
                double venta=Double.valueOf(Venta.getText());
                em.addSalesToEmployee(codigo, venta);   
                
                Codigo.setText("");
                Venta.setText("");
            }catch(IOException e){
                JOptionPane.showMessageDialog(null, "Algo salio mal #.#");
            }catch(InputMismatchException | NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Recuerde Ingresar un valor numerico Oo_oO");
            }
        }
    }//GEN-LAST:event_AgregarVentaBotonActionPerformed

